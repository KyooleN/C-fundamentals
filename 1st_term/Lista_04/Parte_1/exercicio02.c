/******************************************************************************

2. Faça um algoritmo para imprimir os números de 1 a 10 utilizando uma estrutura FAÇA/ENQUANTO e
um contador.

*******************************************************************************/
#include <stdio.h>

int main()
{
  int i;
  i = 1;
  
  do
  {
      printf(" [%d]", i);
      i++;
  }
  while(i <= 10);
}



